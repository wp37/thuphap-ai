type LanguageContextType = {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (key: string, options?: Record<string, any>) => string;
};

const LanguageContext = createContext<LanguageContextType>({ 
  language: 'vi', 
  setLanguage: () => {}, 
  t: (key: string) => key 
});

export const useLanguage = () => useContext(LanguageContext);

export const LanguageProvider: React.FC<PropsWithChildren<{}>> = ({ children }) => {
  const [language, setLanguage] = useState<Language>(() => {
    const saved = localStorage.getItem('tuai_language');
    return (saved as Language) || 'vi';
  });

  useEffect(() => {
    localStorage.setItem('tuai_language', language);
    document.documentElement.lang = language;
  }, [language]);

  const t = (key: string, options?: Record<string, any>): string => {
    const keys = key.split('.');
    let result: any = translations[language];
    for (const k of keys) {
      result = result?.[k];
    }

    if (result === undefined) {
      let current: any = translations[language];
      for (const k of keys) {
        if (!current || typeof current !== 'object') break;
        const lowercaseKeys = Object.keys(current).reduce((acc: any, key) => {
          acc[key.toLowerCase()] = key;
          return acc;
        }, {});
        const realKey = lowercaseKeys[k.toLowerCase()];
        current = current[realKey];
      }
      result = current;
    }

    if (result === undefined) {
        let fallback: any = translations.vi;
        for (const fk of keys) {
            fallback = fallback?.[fk];
        }
        
        if (fallback === undefined) {
            let currVi: any = translations.vi;
            for (const k of keys) {
                if (!currVi || typeof currVi !== 'object') break;
                const lowercaseKeys = Object.keys(currVi).reduce((acc: any, key) => {
                    acc[key.toLowerCase()] = key;
                    return acc;
                }, {});
                const realKey = lowercaseKeys[k.toLowerCase()];
                currVi = currVi[realKey];
            }
            fallback = currVi;
        }

        result = fallback;
    }

    if (typeof result === 'string') {
        if (options) {
            return Object.entries(options).reduce((acc, [optKey, optVal]) => {
                return acc.replace(`{${optKey}}`, String(optVal));
            }, result);
        }
        return result;
    }
    
    return key;
  };

  return React.createElement(LanguageContext.Provider, { value: { language, setLanguage, t } }, children);
};
